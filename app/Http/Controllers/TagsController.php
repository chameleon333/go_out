    {
        $tag = new Tag;
        $tag = $tag::find($id);
        $articles = $tag::find($id)->articles()->paginate(6);
        $popular_tags = $tag->getPopularTags();
        return view('tags.show',[
            'tag' => $tag,
            'articles' => $articles,
            'popular_tags' => $popular_tags,
        ]);
    }

